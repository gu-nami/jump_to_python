b

# 변수를 만드는 여러 가지 방법
# 1. 튜플로 a,b에 값 대응
a, b = ('python', 'life')

# 2. 리스트로 변수 만들기
[a, b] = ['python', 'life']

# 3. 여러 개의 변수에 같은 값 대응
a = b = 'python'

# 두 변수의 값 바꾸기
a = 3
b = 5
a, b = b,a
